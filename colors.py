class Normal:

	gs = "\033[32m"
	rs = "\033[31m"
	ys = "\033[33m"
	ws = "\033[37m"
	bs = "\033[34m"
	ce = "\033[0m"

class Bold:

	gsb = "\033[01;32m"
	rsb = "\033[01;31m"
	ysb = "\033[01;33m"
	wsb = "\033[01;37m"
	bsb = "\033[01;34m"
	ceb = "\033[00m"

class Symbols:

	plus = "\033[32m[+]\033[0m "
	minus = "\033[31m[-]\033[0m "
	astk = "\033[34m[*]\033[0m "
	error = "\033[31m[Error]\033[0m "
